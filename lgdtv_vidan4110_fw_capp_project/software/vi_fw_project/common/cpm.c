void cpm_set_reg(unsigned int addr, unsigned int val, unsigned int mask,
		unsigned int loc) {
	int reg;

	reg = *(volatile unsigned int *) addr;
	reg &= ~(mask << loc);
	reg |= ((val & mask) << loc);
	*(volatile unsigned int *) addr = reg;

	return;
}

